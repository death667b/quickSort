"""
Some basic data types in python3
"""
x = 3
print(type(x), ' = <class \'int\'>')
print(x, ' = 3')
print(x + 1, ' = 4')
print(x - 1, ' = 2')
print(x * 2, ' = 6')
print(x ** 2, ' = 9')

x += 1
print(x, ' = 4')

x *= 2
print(x, ' = 8')

y = 2.5
print(type(y), ' = <class \'float\'>')
print(y, y + 1, y * 2, y ** 2)
print('2.5 3.5 5.0 6.25')